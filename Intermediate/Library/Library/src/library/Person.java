package library;

/**
 *
 * @author Administrator
 */
public abstract class Person{
    private int PID;
    private String name;
    private int age;
    
    public Person(int PID, String name, int age){
        this.PID = PID;
        this.name = name;
        this.age = age;
    }

    public int getPID() {
        return PID;
    }

    public void setPID(int PID) {
        this.PID = PID;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
